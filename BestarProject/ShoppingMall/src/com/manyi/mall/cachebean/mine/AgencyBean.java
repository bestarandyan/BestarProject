public class AgencyBean {

    public String imgUrl;
    public String companyName;
    public String connectName;
    public String connectPhone;
    public String agencyedCityName;

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getConnectName() {
        return connectName;
    }

    public void setConnectName(String connectName) {
        this.connectName = connectName;
    }

    public String getConnectPhone() {
        return connectPhone;
    }

    public void setConnectPhone(String connectPhone) {
        this.connectPhone = connectPhone;
    }

    public String getAgencyedCityName() {
        return agencyedCityName;
    }

    public void setAgencyedCityName(String agencyedCityName) {
        this.agencyedCityName = agencyedCityName;
    }
}
